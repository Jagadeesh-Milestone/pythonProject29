#math module:
import math
#floor division:
print(math.floor(0.5656756))
print(math.floor(4.676))

#Square root:
print(math.sqrt(16))
print(math.sqrt(81))

#factorial:
print(math.factorial(4))

#cos:
print(math.cos(0))
print(math.sin(0))

#lcm:
print(math.lcm(10,20,30,40))

#gcd:
print(math.gcd(10,20,30,40))

#log:
print(math.log(10))
print(math.log(2))

#permutations:
#initialize the no of items to choose from
x=5
#initialize no of items to choose
y=2
print(math.perm(x,y))